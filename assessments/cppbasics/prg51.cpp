/*
Write a program in C++ to check the upper and lower limits of integer.
*/

#include<iostream>

using namespace std;

int main()
{
	cout<< "The upper limit of int datatype is : " << INT_MAX << endl;
	cout<< "The lower limit of int datatype is : " << INT_MIN << endl;
	cout << "The upper limit of unsigned int datatype is : " << UINT_MAX << endl;
	cout << "The upper limit of long long datatype is : " << LLONG_MAX << endl;
	cout << "The lower limit of long long datatype is : " << LLONG_MIN << endl;
	cout << "The upper limit of unsigned long long datatype is : " << ULLONG_MAX << endl;
	cout << "The upper limit of char datatype is : " << CHAR_MAX << endl;
	cout << "The lower limit of char datatype is : " << CHAR_MIN << endl;
	cout << "The upper limit of signed char datatype is : " << SCHAR_MAX << endl;
	cout << "The lower limit of signed char datatype is : " << SCHAR_MIN << endl;
	cout << "The upper limit of unsigned char datatype is : " << UCHAR_MAX << endl;
	cout << "The upper limit of short datatype is : " << SHRT_MAX << endl;
	cout << "The lower limit of short datatype is : " << SHRT_MIN << endl;
	cout << "The upper limit of unsigned short datatype is : " << USHRT_MAX << endl;

	return 0;
}